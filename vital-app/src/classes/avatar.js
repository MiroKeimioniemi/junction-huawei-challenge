class Avatar {
    constructor(name, image, lifeExpectancy, health, strength, speed, jumpHeight) {
      this.name = name;
      this.image = image;
      this.lifeExpectancy = lifeExpectancy
      this.health = health;
      this.strength = strength;
      this.speed = speed;
      this.jumpHeight = jumpHeight;
    }
}

export default Avatar;